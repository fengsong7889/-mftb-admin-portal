/** 需要按界面/广告类型拆分独立 PRD 的算法相关路径 */
const ALGORITHM_PATH = '/promotion-algorithm'
const ALGORITHM_ADD_PATH = '/promotion-algorithm-add'

/**
 * 根据当前路由的 pathname 与 search 构建 PRD 唯一键。
 * 用于将「同一路由下的不同界面」映射到各自独立的 PRD 描述：
 * - 算法庫卡片展示界面：/promotion-algorithm
 * - 算法列表界面（选中某类型后 URL 带 type 参数）：/promotion-algorithm/list
 * - 新增算法界面（按广告类型独立描述）：/promotion-algorithm-add?type=N
 */
export function buildPrdKey(pathname: string, search = ''): string {
  const type = new URLSearchParams(search).get('type')
  if (pathname === ALGORITHM_ADD_PATH && type) {
    return `${ALGORITHM_ADD_PATH}?type=${type}`
  }
  if (pathname === ALGORITHM_PATH && type) {
    return `${ALGORITHM_PATH}/list`
  }
  return pathname
}

/** 获取当前页面的 PRD 描述（key 可能带 query，如 /promotion-algorithm-add?type=1） */
export function getPagePRD(key: string): PagePRD | null {
  // 1. 精确匹配（含带 query 的 key）
  if (pagePRDMap[key]) return pagePRDMap[key]
  // 2. 去除 query 后按 pathname 匹配
  const path = key.split('?')[0]
  if (pagePRDMap[path]) return pagePRDMap[path]
  // 3. 动态路由：去掉最后一段路径匹配父级
  const basePath = path.split('/').slice(0, -1).join('/') || '/'
  return pagePRDMap[basePath] || null
}

export default pagePRDMap
