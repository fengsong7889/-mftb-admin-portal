/** 获取当前页面的 PRD 描述 */
export function getPagePRD(pathname: string): PagePRD | null {
  // 精确匹配
  if (pagePRDMap[pathname]) return pagePRDMap[pathname]
  // 处理带参数的路径
  const basePath = pathname.split('/').slice(0, -1).join('/') || '/'
  return pagePRDMap[basePath] || null
}

export default pagePRDMap
